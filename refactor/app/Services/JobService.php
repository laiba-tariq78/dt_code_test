    /**
     * @param $cuser
     * @return \Illuminate\Database\Eloquent\Collection
     */
    public function getUserJobs($cuser)
    {
        if ($cuser->is('customer')) {
            return $cuser->jobs()
                ->with('user.userMeta', 'user.average', 'translatorJobRel.user.average', 'language', 'feedback')
                ->whereIn('status', ['pending', 'assigned', 'started'])
                ->orderBy('due', 'asc')
                ->get();
        } elseif ($cuser->is('translator')) {
            return Job::getTranslatorJobs($cuser->id, 'new')
                ->pluck('jobs')
                ->all();
        }

        return collect();
    }

    public function prepareJobData(&$data, $consumerType)
    {
        if ($data['immediate'] == 'yes') {
            $data['due'] = Carbon::now()->addMinutes(5)->format('Y-m-d H:i:s');
            $data['customer_phone_type'] = 'yes';
            $data['immediate'] = 'yes';
        } else {
            $due = "{$data['due_date']} {$data['due_time']}";
            $dueCarbon = Carbon::createFromFormat('m/d/Y H:i', $due);
            if ($dueCarbon->isPast()) {
                return $this->buildFailureResponse("Can't create booking in past");
            }
            $data['due'] = $dueCarbon->format('Y-m-d H:i:s');
        }

        $jobFor = $data['job_for'];
        $data['gender'] = in_array('male', $jobFor) ? 'male' : (in_array('female', $jobFor) ? 'female' : null);
        $data['certified'] = $this->getCertifiedType($jobFor);
        $data['customer_phone_type'] = $data['customer_phone_type'] ?? 'no';
        $data['customer_physical_type'] = $data['customer_physical_type'] ?? 'no';
        $data['job_type'] = $this->getJobType($consumerType);
        $data['b_created_at'] = now()->format('Y-m-d H:i:s');
        $data['will_expire_at'] = isset($due) ? TeHelper::willExpireAt($due, $data['b_created_at']) : null;
        $data['by_admin'] = $data['by_admin'] ?? 'no';
    }

    public function translateJobFor($job)
    {
        $jobFor = [];
        if ($job->gender == 'male') {
            $jobFor[] = 'Man';
        } elseif ($job->gender == 'female') {
            $jobFor[] = 'Kvinna';
        }

        if ($job->certified) {
            if ($job->certified == 'both') {
                $jobFor[] = 'normal';
                $jobFor[] = 'certified';
            } else {
                $jobFor[] = $job->certified;
            }
        }

        return $jobFor;
    }

