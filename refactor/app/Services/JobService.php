    private function handleCustomerCancellation($job, $translator)
    {
        $response = [];
        $job->withdraw_at = Carbon::now();
        if ($job->withdraw_at->diffInHours($job->due) >= 24) {
            $job->status = 'withdrawbefore24';
        } else {
            $job->status = 'withdrawafter24';
        }
        $job->save();

        Event::fire(new JobWasCanceled($job));

        $response['status'] = 'success';
        $response['jobstatus'] = $job->status;

        if ($translator) {
            $this->notificationService->notifyTranslatorOfCancellation($job, $translator);
        }

        return $response;
    }

    public function getCertifiedType($jobFor)
    {
        if (in_array('normal', $jobFor)) {
            if (in_array('certified', $jobFor)) return 'both';
            if (in_array('certified_in_law', $jobFor)) return 'n_law';
            if (in_array('certified_in_helth', $jobFor)) return 'n_health';
            return 'normal';
        }
        if (in_array('certified', $jobFor)) return 'yes';
        if (in_array('certified_in_law', $jobFor)) return 'law';
        if (in_array('certified_in_helth', $jobFor)) return 'health';
        return null;
    }

